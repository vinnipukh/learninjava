        public static boolean hasThreeOrMoreConsecutiveConsonants(String word) {
            int consecutiveCount = 0;
            char[] chars = word.toLowerCase().toCharArray();

            for (char c : chars) {
                if (isConsonant(c)) {
                    consecutiveCount++;
                    if (consecutiveCount >= 3) {
                        return true;
                    }
                } else {
                    consecutiveCount = 0;
                }
            }
            return false;
        }

        public static boolean isConsonant(char c) {
            return Character.isLetter(c) && "aeiou".indexOf(c) == -1;
        }

        public static int countWordsWithConsecutiveConsonants(String[] words) {
            int count = 0;
            for (String word : words) {
                if (hasThreeOrMoreConsecutiveConsonants(word)) {
                    count++;
                }
            }
            return count;
        }

